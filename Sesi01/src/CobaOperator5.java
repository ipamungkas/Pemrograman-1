package CobaOperator5.bin;

public class CobaOperator5 {

	public static void main (String[] Xx) {
		int c=3; int d=5; int e=4; int f=6; int g=12;
		
		
		System.out.println("Nilai awal c : " +c);
		c+=7;
		System.out.println("Hasil hitung c = c + 7 : " +c);
		System.out.println ();
		System.out.println ();
		
		
		
		System.out.println("Nilai awal d : " +d);
		d-=4;
		System.out.println("Hasil hitung d = d - 4 : " +d);
		System.out.println ();
		System.out.println ();
				
		
		
		System.out.println("Nilai awal e : " +e);
		e*=5;
		System.out.println("Hasil hitung e = e * 5 : " +e);
		System.out.println ();
		System.out.println ();
		
		
		
		System.out.println("Nilai awal f : " +f);
		f/=3;
		System.out.println("Hasil hitung f = f / 3 : " +f);
		System.out.println ();
		System.out.println ();
		
		
		
		System.out.println("Nilai awal g : " +g);
		g%=7;
		System.out.println("Hasil hitung g = g% 7 : " +g);
		System.out.println ();
		System.out.println ();
	}	
}	